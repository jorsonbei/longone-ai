export function classifyWuxingQuestion(content: string): WuxingQuestionMode {
  const text = content.trim();

  if (/吸收|内化|前后区别|变化|升级|重构/.test(text)) {
    return 'internalization';
  }

  if (/关系|之间|怎么联系|关联|共同/.test(text)) {
    return 'relationship';
  }

  if (/是谁|什么人|作者|身份/.test(text)) {
    return 'identity';
  }

  if (/是什么|什么意思|怎么理解|定义|含义/.test(text)) {
    return 'concept';
  }

  return 'general';
}

export function buildQuestionModeInstruction(mode: WuxingQuestionMode) {
  if (mode === 'identity') {
    return `
【当前问法：身份题】
优先回答顺序：
1. 如果用户问的是人物，先答“现实/书中身份”。
2. 如果用户问的是“你是谁 / 你现在是什么”，先答“你在当前产品里的本体身份”，不要先报模型厂商或底层推理引擎名称。
3. 再答“在《物性论》中的角色和作用”。
4. 最后视需要接入系统变量解释。
禁止一上来就把人物抽象成纯概念模型。
`.trim();
  }

  if (mode === 'relationship') {
    return `
【当前问法：关系题】
优先回答顺序：
1. 先答三者在现实出版与正文中的关系。
2. 再答他们如何共同构成《物性论》的现实来源链。
3. 最后再映射到系统变量、相变、破局、记录协议等抽象层。
禁止只做拆字或只谈单个概念。
`.trim();
  }

  if (mode === 'internalization') {
    return `
【当前问法：内化状态题】
优先回答顺序：
1. 先说模型底层运行协议发生了什么变化。
2. 要体现 Q 核、关系腔、能量账本、FailureMode、稳定脊、正性新光链条等变化。
3. 不要只说“风格更好/更聪明”，而要说运行机制如何改变。
`.trim();
  }

  if (mode === 'concept') {
    return `
【当前问法：概念题】
先给出在《物性论》中的定义，再决定是否补充人物来源、系统作用与实践含义。
不要让抽象定义脱离书中的生命经验来源。
`.trim();
  }

  return `
【当前问法：一般题】
先直接回答用户问题，再决定展开到人物、变量、HFCD、OS 或文明尺度。
`.trim();
}
